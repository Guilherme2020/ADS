'''
	Leia 3 números, calcule e escreva a soma dos 2 primeiros
	 e a diferença entre os 2 últimos.

'''

number_one = int(input(" Digite o primeiro numero  "))
number_two = int(input(" Digite o segundo Numero  "))
number_three = int(input(" Digite o terceiro numero  "))

soma = number_one+number_two
diferen =  number_two-number_three

print("A soma dos dois primeiros numeros eh %d"%(soma))
print("A diferença entre os dois ultimos numeros eh %d"%(diferen))


