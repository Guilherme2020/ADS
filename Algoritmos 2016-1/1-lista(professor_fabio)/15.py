

#Leia o valor da base e altura de um triangulo,
## calcule e escreva sua area. (area=(base * altura)/2)

base = float(input("Digite o valor da base do triangulo: "))
altura = float(input("Digite o valor da altura do triangulo"))

area_triangulo = (base*altura)/2

print("O valor da area do triangulo eh %.2f"%(area_triangulo))

