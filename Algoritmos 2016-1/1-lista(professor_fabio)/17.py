'''

Leia o valor da base e altura de um retangulo, calcule e escreva sua area. (area = base * altura)


'''

base = float(input(" Digite o valor da base do retangulo: "))

altura = float(input("Digite o valor da altura do retangulo: "))

area_retangulo = base*altura

print("O valor da area do retangulo eh %.2f"%(area_retangulo))

